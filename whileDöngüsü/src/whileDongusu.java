
public class whileDongusu {

    public static void main(String[] args) {
        int i = 1;

        while (i < 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("While Dögüsü Bitti.");

        int ı = 1;
        
        while (ı < 10) {
            System.out.println(ı);
            ı += 2;
        }
        System.out.println("While Döngüsü Bitti.");

    }

}
